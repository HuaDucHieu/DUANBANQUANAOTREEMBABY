    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
         // Lấy chuỗi nhập trong ô mã danh mục
        String maDMStr = txtMaDanhMuc.getText().trim();

        // Nếu người dùng chưa nhập mã
        if (maDMStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hoặc nhập Mã Danh Mục để xoá.");
            return; // Dừng lại, không chạy tiếp
        }

        try {
            // Ép kiểu từ chuỗi sang số nguyên
            int maDanhMuc = Integer.parseInt(maDMStr);

            // Gọi hàm xóa trong DAO
            danhMucDao.delete(maDanhMuc);

            // Thông báo xóa thành công
            JOptionPane.showMessageDialog(this, "Xoá danh mục thành công!");

            // Làm mới lại bảng sau khi xóa
            this.fillTable();

        } catch (NumberFormatException e) {
            // Nếu người dùng nhập ký tự không phải số
            JOptionPane.showMessageDialog(this, "Mã danh mục không hợp lệ!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed


