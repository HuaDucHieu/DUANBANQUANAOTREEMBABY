    // ✅ Hàm này lấy dữ liệu từ CSDL và đổ lên bảng tblDanhMuc
    public void fillTable() {
        // Ép kiểu model của JTable để có thể thao tác thêm dòng/xóa dòng
        DefaultTableModel model = (DefaultTableModel) tblDanhMuc.getModel();
        model.setRowCount(0); // Xóa tất cả dữ liệu cũ trong bảng

        // Lặp qua danh sách danh mục lấy từ database
        for (DanhMucEntity danhmuc : danhMucDao.getAll()) {
            Object data[] = {
                danhmuc.getMaDanhMuc(), // Lấy mã danh mục
                danhmuc.getTenDanhMuc(), // Lấy tên danh mục
                danhmuc.getMoTa()        // Lấy mô tả danh mục
            };
            model.addRow(data); // Thêm 1 dòng mới vào bảng
        }

        // Gán model đã cập nhật lại cho bảng
        tblDanhMuc.setModel(model);
    }

    // ✅ Lấy thông tin từ các ô nhập (TextField) để tạo ra đối tượng DanhMucEntity
    public DanhMucEntity getDanhMuc() {
        try {
            int maDM = 1; // Nếu ô mã danh mục trống, mặc định là 1 (hoặc bỏ qua nếu tự tăng)
            if (!txtMaDanhMuc.getText().isEmpty()) {
                maDM = Integer.parseInt(txtMaDanhMuc.getText()); // Ép kiểu từ chuỗi sang số
            }

            // Lấy tên danh mục từ ô nhập
            String tenDM = txtTenDanhMuc.getText();

            // Lấy mô tả từ ô nhập
            String moTa = txtMoTa.getText();

            // Trả về 1 đối tượng DanhMucEntity chứa thông tin người dùng nhập
            return new DanhMucEntity(maDM, tenDM, moTa);

        } catch (Exception e) {
            // Nếu có lỗi (ví dụ: nhập chữ vào ô số), in lỗi ra console
            System.out.println("Lỗi get danh mục: " + e.getMessage());
            return null;
        }
    }

    // ✅ Hiển thị thông tin danh mục lên form khi người dùng chọn 1 dòng trên bảng
